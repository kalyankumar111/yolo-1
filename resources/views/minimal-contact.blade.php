          {
            stylers: [
              { hue: "#c5c5c5" },
              { saturation: -100 }
            ]
          },
        ];
        map.setOptions({styles: styles});
        marker.setMap(map);
    }
    google.maps.event.addDomListener(window, 'load', initialize);
